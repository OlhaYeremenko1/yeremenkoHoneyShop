<h3>Напиток из овса</h3>
 Вода - 1,5 л<br>
 овес -1,5 стакана<br>
 мед -1 ст. ложка<br>
 лимон - 0,5 шт. <br>
 Выход - 1 л. <br>
  
<p>Овес залить холодной водой и довести до кипения. Настаивать в теплом месте 3-4 ч. В конце настаивания до¬бавить мелко нарезанную или натертую на крупной терке цедру лимо¬на, процедить, добавить мед, лимонный сок. Подавать теплым.</p>
<p>Напитки, приготовленные на основе овсяных отваров и пшеничных отрубей, рекомендуются не только как утоляющие жажду, но и в лечебных целях людям, страдающим заболеваниями сердечно-со¬судистой системы, желудочно-кишечного тракта, нарушением об¬мена веществ; с добавлением сушеных или свежих фруктов эти на-питки приобретают свойства общеукрепляющих, тонизирующих. </p>  
