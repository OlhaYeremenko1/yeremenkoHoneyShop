<p>Вопрос долголетия волновал умы людей всех времен, стран и народов. Сб этом свидетельствуют иероглифы Древнего Египта и Ки¬тая, древние памятники Индии и Греции. Для поддержания здоровой и долгой жизни необходим правильный режим питания. Мыслители и врачи древности придавали меду огромное значение, считая, что употребление его в пищу способствует продлению человеческой жиз¬ни. Нередко люди, регулярно питающиеся медом, доживают до глубо¬кой старости. </p>
<p>Долгое время мед был основным источни¬ком сладости для человека, поэтому наложил отпечаток на меню сладкого стола в Европе. Преимущест¬венно мед использовался в кондитерских изделиях (пряниках, ков¬рижках и др.)« а также в напитках, на Среднем и Ближнем Востоке -для заливки горячих кондитерских изделий (например пахлавы). После появления сахара употребление меда в кулинарных и конди¬терских изделиях резко сократилось. Но сегод¬ня, оценив максимальную полезность меда по его витаминному и ми¬неральному составам, осознав вредность рафинированных продуктов, в том числе сахара, люди вновь обратились к натуральному продук¬ту - меду. </p>
<p>Предлагаемые рецепты мучных кондитерских изделий, холодных и горячих медовых напитков, овощных и фруктовых блюд с медом при правильном их подборе и систематическом употреблении воспол¬нят пробел в вашем ежедневном питании и позволят корректировать витаминную и минеральную ценность суточного рациона на протяже¬нии всего года, продлят молодость, активное долголетие. </p>
    
